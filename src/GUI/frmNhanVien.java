    public void laydulieuNhanVien(ArrayList<XuLy.NhanVien> dsNV){
        table.removeAll();
        tblTitle.removeAllElements();
        bangNV();
        for(XuLy.NhanVien b : dsNV){
           
            Vector v = new Vector();
            v.add(b.getMaNV()); 
            v.add(b.getHoten()); 
            v.add(b.getDiaChi());
            v.add(b.getSDT());
            v.add(b.getNgayVL());
            v.add(b.getPHAI());
            v.add(b.getNgaySinh());
            tblData.add(v);
        }
        table.setModel(new DefaultTableModel(tblData, tblTitle));
    }
