    public static String phatSinhMa(String KTDau)
            {
                String a = "";
                try {
                int i = Integer.parseInt(NhanVienDAO.getMaNVCuoi().substring(2, 5));
                i++;
                if(i < 10) return a = KTDau + "00" + i;
                else if(i >= 10 && i < 100) return a = KTDau + "0" + i;
                else return a = KTDau + i;
                } catch(NumberFormatException e){}
                return a;
            }
