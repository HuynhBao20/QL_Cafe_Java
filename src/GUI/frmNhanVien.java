    public void laydulieuChamCong(ArrayList<ChamCong> ds){
        tblData.removeAllElements();
        table.removeAll();
        tblTitle.removeAllElements();
        bangCC();
        for(ChamCong b : ds){
           
            Vector v = new Vector();
            v.add(b.getMaNV()); 
            v.add(b.getNgayDD()); 
            v.add(b.getSoGio());
           
            tblData.add(v);
        }
        table.setModel(new DefaultTableModel(tblData, tblTitle));
    }
